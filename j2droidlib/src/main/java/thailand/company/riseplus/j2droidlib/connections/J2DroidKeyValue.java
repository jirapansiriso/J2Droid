package thailand.company.riseplus.j2droidlib.connections;

public class J2DroidKeyValue {

    private String key;
    private String value;

    public J2DroidKeyValue(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
